  shop_id integer NOT NULL,
  postal_no integer NOT NULL,
  changed_id integer NOT NULL,
  current bool NOT NULL,
  PRIMARY KEY(shop_id, changed_id)
);
CREATE TABLE changed (
  changed_id integer NOT NULL PRIMARY KEY,
  type varchar(10), 
  changedate timestamp,
  comments text NOT NULL DEFAULT ''
